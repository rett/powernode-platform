# Main execution
main() {
    echo "🔍 Configuration Type: $SETUP_TYPE"
    echo "🌐 Proxy Host: $PROXY_HOST"
    echo "🔒 Protocol: $PROXY_PROTOCOL"
    echo ""
    
    # Create necessary directories
    mkdir -p "$FRONTEND_DIR/scripts"
    mkdir -p "$SERVER_DIR/config"
    
    # Run setup functions
    setup_frontend_env
    setup_vite_external_config
    setup_nginx_config
    setup_backend_proxy
    create_startup_script
    
    # Display summary
    display_summary
}

# Handle command line arguments
case "$1" in
    --help|-h)
        echo "Usage: $0 [external|local] [PROXY_HOST] [PROXY_PROTOCOL]"
        echo ""
        echo "Examples:"
        echo "  $0                    # Use defaults (external, dev-1.ipnode.net, https)"
        echo "  $0 local              # Local development setup"
        echo "  $0 external prod.example.com https"
        echo ""
        echo "Environment variables:"
        echo "  PROXY_HOST     - Proxy hostname (default: dev-1.ipnode.net)"
        echo "  PROXY_PROTOCOL - Protocol to use (default: https)"
        echo "  BACKEND_PORT   - Backend port (default: 3000)"
        echo "  FRONTEND_PORT  - Frontend port (default: 3001)"
        exit 0
        ;;
    local)
        PROXY_HOST="localhost"
        PROXY_PROTOCOL="http"
        ;;
    external)
        # Use defaults or environment variables
        ;;
    *)
        # Custom configuration
        if [ -n "$2" ]; then
            PROXY_HOST="$2"
        fi
        if [ -n "$3" ]; then
            PROXY_PROTOCOL="$3"
        fi
        ;;
esac

# Run main setup
main