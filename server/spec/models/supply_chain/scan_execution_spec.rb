  end

  # ============================================
  # Instance Methods - Status Transitions
  # ============================================
  describe "#start!" do
    it "updates status to running and sets started_at" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending", started_at: nil)

      expect {
        execution.start!
      }.to change {
        execution.reload.status
      }.from("pending").to("running")

      expect(execution.started_at).to be_present
    end

    it "raises error if execution is not found" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account)
      execution_id = execution.id
      execution.delete

      expect {
        SupplyChain::ScanExecution.find(execution_id).start!
      }.to raise_error(ActiveRecord::RecordNotFound)
    end
  end

  describe "#complete!" do
    it "updates status to completed and sets completed_at and duration_ms" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running", started_at: 5.seconds.ago)

      expect {
        execution.complete!({ "result" => "success" })
      }.to change {
        execution.reload.status
      }.from("running").to("completed")

      expect(execution.completed_at).to be_present
      expect(execution.duration_ms).to be_present
      expect(execution.output_data).to eq({ "result" => "success" })
    end

    it "sets output_data to empty hash if not provided" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running", started_at: 1.second.ago)
      execution.complete!

      expect(execution.output_data).to eq({})
    end

    it "calculates duration_ms from started_at and current time" do
      started = 5.seconds.ago
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running", started_at: started)

      execution.complete!

      expect(execution.duration_ms).to be_a(Integer)
      expect(execution.duration_ms).to be > 0
    end
  end

  describe "#fail!" do
    it "updates status to failed and sets completed_at, duration_ms, and error_message" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running", started_at: 3.seconds.ago)

      expect {
        execution.fail!("Connection timeout")
      }.to change {
        execution.reload.status
      }.from("running").to("failed")

      expect(execution.completed_at).to be_present
      expect(execution.duration_ms).to be_present
      expect(execution.error_message).to eq("Connection timeout")
    end

    it "preserves error message with special characters" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running", started_at: 1.second.ago)
      error_msg = 'Error: "invalid JSON" @ line 42'

      execution.fail!(error_msg)

      expect(execution.error_message).to eq(error_msg)
    end
  end

  describe "#cancel!" do
    it "updates status to cancelled and sets completed_at and duration_ms" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending", started_at: 5.minutes.ago)

      expect {
        execution.cancel!
      }.to change {
        execution.reload.status
      }.from("pending").to("cancelled")

      expect(execution.completed_at).to be_present
      expect(execution.duration_ms).to be_present
    end

    it "calculates duration even if not started" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending", started_at: nil)
      execution.cancel!

      expect(execution.duration_ms).to be_nil
    end
  end

