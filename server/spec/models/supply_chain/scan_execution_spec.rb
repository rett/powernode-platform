  # ============================================
  # Instance Methods - Summary Data
  # ============================================
  describe "#summary" do
    it "returns a hash with execution summary" do
      user = create(:user)
      execution = create(:supply_chain_scan_execution,
                        scan_instance: scan_instance,
                        account: account,
                        triggered_by: user,
                        status: "completed",
                        trigger_type: "manual",
                        started_at: 10.seconds.ago,
                        completed_at: Time.current,
                        duration_ms: 10000,
                        error_message: nil)

      summary = execution.summary

      expect(summary).to be_a(Hash)
      expect(summary[:id]).to eq(execution.id)
      expect(summary[:execution_id]).to eq(execution.execution_id)
      expect(summary[:scan_instance_id]).to eq(scan_instance.id)
      expect(summary[:instance_name]).to eq(scan_instance.name)
      expect(summary[:template_name]).to eq(scan_template.name)
      expect(summary[:status]).to eq("completed")
      expect(summary[:trigger_type]).to eq("manual")
      expect(summary[:triggered_by_id]).to eq(user.id)
      expect(summary[:started_at]).to eq(execution.started_at)
      expect(summary[:completed_at]).to eq(execution.completed_at)
      expect(summary[:duration_ms]).to eq(10000)
      expect(summary[:formatted_duration]).to eq("10s")
      expect(summary[:successful]).to be true
      expect(summary[:error_message]).to be_nil
      expect(summary[:created_at]).to eq(execution.created_at)
    end

    it "includes error message when execution failed" do
      execution = create(:supply_chain_scan_execution,
                        scan_instance: scan_instance,
                        account: account,
                        status: "failed",
                        error_message: "Scan failed due to network error")

      summary = execution.summary

      expect(summary[:status]).to eq("failed")
      expect(summary[:successful]).to be false
      expect(summary[:error_message]).to eq("Scan failed due to network error")
    end

    it "handles nil triggered_by" do
      execution = create(:supply_chain_scan_execution,
                        scan_instance: scan_instance,
                        account: account,
                        triggered_by: nil)

      summary = execution.summary

      expect(summary[:triggered_by_id]).to be_nil
    end
  end

