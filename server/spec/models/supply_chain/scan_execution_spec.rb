# frozen_string_literal: true

require "rails_helper"

RSpec.describe SupplyChain::ScanExecution, type: :model do
  include ActiveSupport::Testing::TimeHelpers

  let(:account) { create(:account) }
  let(:scan_template) { create(:supply_chain_scan_template) }
  let(:scan_instance) { create(:supply_chain_scan_instance, account: account, scan_template: scan_template) }
  let(:triggered_by_user) { create(:user) }

  # ============================================
  # Constants
  # ============================================
  describe "constants" do
    it "defines STATUSES" do
      expect(SupplyChain::ScanExecution::STATUSES).to eq(%w[pending running completed failed cancelled])
    end

    it "defines TRIGGER_TYPES" do
      expect(SupplyChain::ScanExecution::TRIGGER_TYPES).to eq(%w[manual scheduled webhook pipeline api])
    end
  end

  # ============================================
  # Associations
  # ============================================
  describe "associations" do
    it { is_expected.to belong_to(:scan_instance).class_name("SupplyChain::ScanInstance") }
    it { is_expected.to belong_to(:account) }
    it { is_expected.to belong_to(:triggered_by).class_name("User").optional }
  end

  # ============================================
  # Validations
  # ============================================
  describe "validations" do
    subject { build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account) }

    # Note: execution_id is auto-generated by before_validation callback,
    # so shoulda-matchers can't test presence directly
    it "requires execution_id" do
      execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account)
      execution.execution_id = nil
      expect(execution).not_to be_valid
      expect(execution.errors[:execution_id]).to include("can't be blank")
    end

    it "validates uniqueness of execution_id" do
      first = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account)
      duplicate = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, execution_id: first.execution_id)
      expect(duplicate).not_to be_valid
      expect(duplicate.errors[:execution_id]).to include("has already been taken")
    end

    it { is_expected.to validate_presence_of(:status) }
    it { is_expected.to validate_inclusion_of(:status).in_array(SupplyChain::ScanExecution::STATUSES) }
    it { is_expected.to validate_presence_of(:trigger_type) }
    it { is_expected.to validate_inclusion_of(:trigger_type).in_array(SupplyChain::ScanExecution::TRIGGER_TYPES) }
  end

  # ============================================
  # Scopes
  # ============================================
  describe "scopes" do
    let!(:pending_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending")
    end
    let!(:running_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "running")
    end
    let!(:completed_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "completed")
    end
    let!(:failed_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "failed")
    end
    let!(:cancelled_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "cancelled")
    end
    let!(:manual_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, trigger_type: "manual")
    end
    let!(:scheduled_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, trigger_type: "scheduled")
    end
    let!(:webhook_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, trigger_type: "webhook")
    end
    let!(:pipeline_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, trigger_type: "pipeline")
    end
    let!(:api_execution) do
      create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, trigger_type: "api")
    end
    let!(:another_instance_execution) do
      another_instance = create(:supply_chain_scan_instance, account: account)
      create(:supply_chain_scan_execution, scan_instance: another_instance, account: account)
    end

    describe "#by_status" do
      it "filters executions by status" do
        expect(described_class.by_status("pending")).to include(pending_execution)
        expect(described_class.by_status("pending")).not_to include(completed_execution)
      end
    end

    describe "#pending" do
      it "returns only pending executions" do
        expect(described_class.pending).to include(pending_execution)
        expect(described_class.pending).not_to include(running_execution, completed_execution)
      end
    end

    describe "#running" do
      it "returns only running executions" do
        expect(described_class.running).to include(running_execution)
        expect(described_class.running).not_to include(pending_execution, completed_execution)
      end
    end

    describe "#completed" do
      it "returns only completed executions" do
        expect(described_class.completed).to include(completed_execution)
        expect(described_class.completed).not_to include(pending_execution, failed_execution)
      end
    end

    describe "#failed" do
      it "returns only failed executions" do
        expect(described_class.failed).to include(failed_execution)
        expect(described_class.failed).not_to include(pending_execution, completed_execution)
      end
    end

    describe "#cancelled" do
      it "returns only cancelled executions" do
        expect(described_class.cancelled).to include(cancelled_execution)
        expect(described_class.cancelled).not_to include(pending_execution, completed_execution)
      end
    end

    describe "#finished" do
      it "returns completed, failed, and cancelled executions" do
        expect(described_class.finished).to include(completed_execution, failed_execution, cancelled_execution)
        expect(described_class.finished).not_to include(pending_execution, running_execution)
      end
    end

    describe "#successful" do
      it "returns only successful (completed) executions" do
        expect(described_class.successful).to include(completed_execution)
        expect(described_class.successful).not_to include(failed_execution, pending_execution)
      end
    end

    describe "#by_trigger" do
      it "filters executions by trigger type" do
        expect(described_class.by_trigger("manual")).to include(manual_execution)
        expect(described_class.by_trigger("manual")).not_to include(scheduled_execution)
      end
    end

    describe "#manual" do
      it "returns only manually triggered executions" do
        expect(described_class.manual).to include(manual_execution)
        expect(described_class.manual).not_to include(scheduled_execution, webhook_execution)
      end
    end

    describe "#scheduled" do
      it "returns only scheduled executions" do
        expect(described_class.scheduled).to include(scheduled_execution)
        expect(described_class.scheduled).not_to include(manual_execution, webhook_execution)
      end
    end

    describe "#for_instance" do
      it "returns executions for a specific instance" do
        expect(described_class.for_instance(scan_instance.id)).to include(pending_execution, running_execution, completed_execution)
        expect(described_class.for_instance(scan_instance.id)).not_to include(another_instance_execution)
      end
    end

    describe "#recent" do
      it "returns executions ordered by created_at descending" do
        executions = described_class.recent
        expect(executions.first.created_at).to be >= executions.last.created_at
      end
    end
  end

  # ============================================
  # Callbacks
  # ============================================
  describe "callbacks" do
    describe "before_validation :generate_execution_id" do
      it "generates execution_id before validation on create" do
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, execution_id: nil)
        expect(execution.execution_id).to be_nil

        execution.valid?

        expect(execution.execution_id).to be_present
        expect(execution.execution_id).to match(/^exec-\d{14}-[0-9a-f]{8}$/)
      end

      it "does not overwrite existing execution_id" do
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, execution_id: "custom-id-123")
        execution.valid?

        expect(execution.execution_id).to eq("custom-id-123")
      end
    end

    describe "before_save :sanitize_jsonb_fields" do
      it "initializes input_data to empty hash if nil" do
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, input_data: nil)
        execution.save!

        expect(execution.input_data).to eq({})
      end

      it "initializes output_data to empty hash if nil" do
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, output_data: nil)
        execution.save!

        expect(execution.output_data).to eq({})
      end

      it "initializes metadata to empty hash if nil" do
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, metadata: nil)
        execution.save!

        expect(execution.metadata).to eq({})
      end

      it "preserves existing JSONB values" do
        input = { "key" => "value" }
        output = { "result" => "success" }
        execution = build(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, input_data: input, output_data: output)
        execution.save!
        execution.reload

        expect(execution.input_data).to eq(input)
        expect(execution.output_data).to eq(output)
      end
    end

    describe "after_save :update_instance_stats" do
      context "when status changes to completed" do
        it "calls update_instance_stats on the scan_instance" do
          execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending")
          allow(scan_instance).to receive(:record_execution_result!)

          execution.update!(status: "completed", completed_at: Time.current)

          expect(scan_instance).to have_received(:record_execution_result!).with(success: true)
        end
      end

      context "when status changes to failed" do
        it "calls update_instance_stats on the scan_instance" do
          execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending")
          allow(scan_instance).to receive(:record_execution_result!)

          execution.update!(status: "failed", completed_at: Time.current)

          expect(scan_instance).to have_received(:record_execution_result!).with(success: false)
        end
      end

      context "when status does not change" do
        it "does not call update_instance_stats" do
          execution = create(:supply_chain_scan_execution, scan_instance: scan_instance, account: account, status: "pending")
          allow(scan_instance).to receive(:record_execution_result!)

          execution.update!(logs: "new log entry")

          expect(scan_instance).not_to have_received(:record_execution_result!)
        end
      end
    end
  end

